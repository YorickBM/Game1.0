package display;

public class Display {
	
	private static int Width;
	private static int Height;
	
	public static int getWidth() {
		return Width;
	}
	public static void setWidth(int width) {
		Width = width;
	}
	
	public static int getHeight() {
		return Height;
	}
	public static void setHeight(int height) {
		Height = height;
	}
	
	

}
